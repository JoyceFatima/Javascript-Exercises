function Numero(extenso) {
  switch(extenso) {
    case 1:
      console.log(`Seu número é um`)
      break;
    case 2:
      console.log(`Seu número é dois`)
      break;
    case 3:
      console.log(`Seu número é três`)
      break;
    case 4:
      console.log(`Seu número é quatro`)
      break;
    case 5:
      console.log(`Seu numero é cinco`)
      break;
    case 6:
      console.log(`Seu numero é seis`)
      break;
    case 7:
      console.log(`Seu numero é sete`)
      break;
    case 8:
      console.log(`Seu numero é oito`)
      break;
    case 9:
      console.log(`Seu numero é nove`)
      break;
    case 10:
      console.log(`Seu numero é dez`)
      break;
      default:
      console.log(`Número fora do intervalo`);
  }
}

Numero(7)
